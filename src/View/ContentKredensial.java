    private void LoadData() {
        DefaultTableModel model = (DefaultTableModel) TableKredensial.getModel();
        var data = Model.Kredensial.Get();
        var rows = new Object[4];
        for (HashMap<String, String> item : data) {
            rows[0] = item.get("id");
            rows[1] = item.get("username");
            rows[2] = item.get("password");
            rows[3] = item.get("level");
            model.addRow(rows);
        }
    }
    
    private void ClearData() {
        DefaultTableModel model = (DefaultTableModel) TableKredensial.getModel();
        for (int i = model.getRowCount() - 1; i > -1; i--) {
            model.removeRow(i);
        }
    }
    
    private void FieldClear() {
        fieldUsername.setText("");
        fieldPassword.setText("");
        cboxLevel.setSelectedIndex(-1);
    }

