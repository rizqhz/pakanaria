    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        var username = fieldUsername.getText();
        var password = classes.Helper.CreateSha256(fieldPassword.getText());
        var level = cboxLevel.getSelectedItem().toString();
        Model.Kredensial.Add(username, password, level);
        this.ClearData();
        this.LoadData();
        this.FieldClear();
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int res = JOptionPane.showConfirmDialog(rootPane, "Apakah anda yakin?", "Hapus Data", JOptionPane.YES_NO_OPTION);
        if (res == JOptionPane.YES_OPTION) {
            Model.Kredensial.Destroy(key);
            this.ClearData();
            this.LoadData();
            this.FieldClear();
        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        var username = fieldUsername.getText();
        var password = classes.Helper.CreateSha256(fieldPassword.getText());
        var level = cboxLevel.getSelectedItem().toString();
        Model.Kredensial.Edit(key, username, password, level);
        this.ClearData();
        this.LoadData();
        this.FieldClear();
    }//GEN-LAST:event_btnUbahActionPerformed

    private void TableKredensialMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableKredensialMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) TableKredensial.getModel();
        int idx = TableKredensial.getSelectedRow();
        key = Integer.parseInt(model.getValueAt(idx, 0).toString());
        fieldUsername.setText(model.getValueAt(idx, 1).toString());
        fieldPassword.setText(model.getValueAt(idx, 2).toString());
        cboxLevel.setSelectedItem(model.getValueAt(idx, 3).toString());
    }//GEN-LAST:event_TableKredensialMouseClicked

