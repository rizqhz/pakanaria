    private void LoadData() {
        DefaultTableModel model = (DefaultTableModel) TablePakan.getModel();
        var data = Model.Pakan.Get();
        var rows = new Object[8];
        for (HashMap<String, String> item : data) {
            rows[0] = item.get("id");
            rows[1] = item.get("kode");
            rows[2] = item.get("nama");
            rows[3] = item.get("kategori");
            rows[4] = item.get("hewan");
            rows[5] = item.get("deskripsi");
            rows[6] = item.get("satuan_id");
            rows[7] = item.get("supplier_id");
            model.addRow(rows);
        }
    }
    
    private void ClearData() {
        DefaultTableModel model = (DefaultTableModel) TablePakan.getModel();
        for (int i = model.getRowCount() - 1; i > -1; i--) {
            model.removeRow(i);
        }
    }
    
