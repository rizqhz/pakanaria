    private void TablePakanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablePakanMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) TablePakan.getModel();
        int idx = TablePakan.getSelectedRow();
        key = Integer.parseInt(model.getValueAt(idx, 0).toString());
        fieldKode.setText(model.getValueAt(idx, 1).toString());
        fieldNama.setText(model.getValueAt(idx, 2).toString());
        cboxKategori.setSelectedItem(model.getValueAt(idx, 3).toString());
        fieldHewan.setText(model.getValueAt(idx, 4).toString());
        fieldDeskripsi.setText(model.getValueAt(idx, 5).toString());
        cboxSatuan.setSelectedItem(Model.Satuan.Get(Integer.parseInt(model.getValueAt(idx, 6).toString())).get(0).get("satuan"));
        cboxSupplier.setSelectedItem(Model.Supplier.Get(Integer.parseInt(model.getValueAt(idx, 7).toString())).get(0).get("nama"));
    }//GEN-LAST:event_TablePakanMouseClicked

