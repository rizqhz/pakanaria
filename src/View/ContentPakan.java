    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        var kode = fieldKode.getText();
        var nama = fieldNama.getText();
        var kategori = cboxKategori.getSelectedItem().toString();
        var hewan = fieldHewan.getText();
        var satuan = Model.Satuan.Get(cboxSatuan.getSelectedItem().toString());
        var supplier = Model.Supplier.Get(cboxSupplier.getSelectedItem().toString());
        var deskripsi = fieldDeskripsi.getText();
        Model.Pakan.Add(kode, nama, kategori, hewan, deskripsi, satuan.get(0).get("id"), supplier.get(0).get("id"));
        this.ClearData();
        this.LoadData();
        this.FieldClear();
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int res = JOptionPane.showConfirmDialog(rootPane, "Apakah anda yakin?", "Hapus Data", JOptionPane.YES_NO_OPTION);
        if (res == JOptionPane.YES_OPTION) {
            Model.Pakan.Destroy(key);
            this.ClearData();
            this.LoadData();
            this.FieldClear();
        }
    }//GEN-LAST:event_btnHapusActionPerformed

