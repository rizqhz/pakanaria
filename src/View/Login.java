    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        String username = fieldUsername.getText();
        String password = classes.Helper.CreateSha256(fieldPassword.getText());
        var res = App.Auth.Login(username, password);
        if (res) {
            JOptionPane.showMessageDialog(rootPane, "Proses Login Berhasil!", "Pemberitahuan", JOptionPane.OK_OPTION);
            Dashboard dashboard = new Dashboard();
            dashboard.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Proses Login Gagal!", "Pemberitahuan", JOptionPane.OK_OPTION);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

